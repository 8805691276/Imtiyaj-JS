let a = 5;
let b = 7;

console.log("a = ", a ,"& b = ",b);

a++;  // a = a + 1
console.log("a = ",a);

a--; // a - a - 1
console.log("a = ",a);

b++; //b = b + 1
console.log("b = ",b);

b--; //b = b - 1
console.log("b = ",b);

//----------------------------------------------------------------
//PreIncreament & PostIncreament :
let c = 8;
let d = 6;

console.log("c++ = ",c++);    //Post-Increament...
console.log("c = ",c);    
console.log("++c = ",++c);    //Pre-Increament...

console.log("d-- = ",d--);    //Post-Increament...
console.log("d = ",d);    
console.log("--d = ",--d);    //Pre-Increament...