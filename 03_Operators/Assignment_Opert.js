let a = 5;
let b = 2;

a += 4; // a = a + 4
console.log("a = ",a);

b += 8; //b = b + 8
console.log("b = ",b);

a -= 4; // a = a - 4
console.log("a = ",a);

b -= 8; //b = b - 8
console.log("b = ",b);

a *= 4; // a = a * 4
console.log("a = ",a);

b *= 8; //b = b * 8
console.log("b = ",b);

a /= 4; // a = a / 4
console.log("a = ",a);

b /= 8; //b = b / 8
console.log("b = ",b);

a %= 4; // a = a + 4
console.log("a = ",a);

b %= 8; //b = b + 8
